# Ejemplo for
frutas = ['banana', 'manzana', 'pera', 'naranja']
for f in frutas:
    print('La palabra', f, 'tiene', len(f), 'letras')

print('Imprime del 0 al 9:')
for i in range(10):
    print('  ', i)

print('Imrpime del 5 al 9: ')
for i in range(5, 10):
    print('  ', i)

print('Imprime del 0 al 9 de dos en dos:')
for i in range(0, 9, 2):
    print('  ', i)

